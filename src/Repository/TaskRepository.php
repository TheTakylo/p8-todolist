    public function findForAdmin(UserInterface $user)
    {
        return $this->createQueryBuilder('p')
            ->where('p.user = :user')
            ->setParameter('user', $user)
            ->orWhere('p.user is null')
            ->getQuery()
            ->getResult();
    }

